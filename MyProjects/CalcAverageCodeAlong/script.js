// Input
let x = prompt("Enter your first number!");
let num1 = parseInt(x);
let y = prompt("Enter your second number!");
let num2 = parseInt(y);
let z = prompt("Enter your third number!");
let num3 = parseInt(z);

// Process
let average = (num1 + num2 + num3) / 3;

// Output
alert(`Your average is: ${average}`);
